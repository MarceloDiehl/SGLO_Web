    private void btnCadastrarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarProdutoActionPerformed
        // TODO add your handling code here:

        String strTitulo = txtTitulo.getText();
        String strDescricao = txtDescricao.getText();
        String strDataInclusao = txtData.getText();
        String strValorUnitario = txtValor.getText();
        String strQuantidade = txtQuantidade.getText();
        int strTipo = cmbTipo.getSelectedIndex();
        int strCondicao = cmbCondicao.getSelectedIndex();
        String strCodigo = txtCodigoBarras.getText();

        if (strTitulo.isEmpty() || strDescricao.isEmpty() || strDataInclusao.isEmpty() || strValorUnitario.isEmpty() || strQuantidade.isEmpty() || strTipo == 0 || strCondicao == 0 || strCodigo.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos. ");
        } else {
            //Teste de validação de entrada de Data:
            String strTesteData = txtData.getText();
            boolean testeData = strTesteData.matches("^(0[1-9]|[12][0-9]|3[01])/(0[1-9]|1[0-2])/(\\d{4})$");

            //Teste de validação de entrada de código de barras:
            String strTesteCodigo = txtCodigoBarras.getText();
            boolean testeCodigo = strTesteCodigo.matches("[0-9]{6}");

            //Teste de validação de entrada de valores:
            String strTesteValorUnitario = txtValor.getText();
            boolean testeValor = strTesteValorUnitario.matches("\\d+(\\.\\d{1,2})?");

            //Teste de validação de entrada de quantidade:
            String strTesteQuantidade = txtQuantidade.getText();
            boolean testeQuantidade = strTesteQuantidade.matches("\\d+");

            if (testeCodigo == true && testeData == true && testeValor == true && testeQuantidade == true) {
                CadastroProduto p = new CadastroProduto();
                p.setCodProduto(txtCodigoBarras.getText());
                p.setTitulo(txtTitulo.getText());
                p.setValor(Double.parseDouble(txtValor.getText()));
                p.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
                p.setEntrada(convertLocalDate(txtData.getText()));
                p.setIdTipo(cmbTipo.getSelectedIndex());
                p.setIdCondicao(cmbCondicao.getSelectedIndex());
                p.setDescricao(txtDescricao.getText());

                CadastroDAO catalogoDAO = new CadastroDAO();
                catalogoDAO.CadastrarProduto(p);
                JOptionPane.showMessageDialog(null, "Cadastro de novo produto realizado com sucesso! \n");

                limparCampos();

            } else {
                JOptionPane.showMessageDialog(null, "As datas devem seguir o formato dd/mm/aaaa. \n" + "O código deverá ter sequencia numérica de 6 dígitos. \n" + "A quantidade mínima será 1. \n" + "Os valores deverão estar em x.xx com valor mínimo de 0.01. \n " + "Os campos de escolha deverão ser selecionados. \n");
            }

        }


    }//GEN-LAST:event_btnCadastrarProdutoActionPerformed

