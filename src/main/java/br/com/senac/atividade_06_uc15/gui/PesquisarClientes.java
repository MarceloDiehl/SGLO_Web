    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linha = tblClientes.getSelectedRow();

        if (linha != -1) {
            String nomeColuna = "ID";
            int coluna = tblClientes.getColumnModel().getColumnIndex(nomeColuna);

            Object id = tblClientes.getValueAt(linha, coluna);

            if (id != null) {
                try {
                    int valorID = Integer.parseInt(id.toString());

                    CadastroDAO cadastroClienteDAO = new CadastroDAO();
                    cadastroClienteDAO.excluirCliente(valorID);
                    JOptionPane.showMessageDialog(null, "Cliente exclu√≠do com sucesso.\n");
                    preencherTabela(cadastroClienteDAO.listarCliente(txtCPF.getText()));
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Erro ao converter o ID para n√∫mero.\n");
                }
            } else {
                JOptionPane.showMessageDialog(null, "O valor do ID est√° vazio.\n");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um cliente da lista.\n");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

