    public void preencherTabela(List<CadastroCliente> cadastroCliente) {
        String colunas[] = {
            "ID", "Nome", "Sobrenome", "Data de Nascimento", "CPF", "RG", "Endereco", "Numero Predial", "Bairro", "Cidade", "UF", "CEP", "Telefone", "Cliente Desde"
        };

        String dados[][] = new String[cadastroCliente.size()][colunas.length];

        int i = 0;
        for (CadastroCliente c : cadastroCliente) {
            dados[i] = new String[]{
                String.valueOf(c.getId()),
                c.getNome(),
                c.getSobrenome(),
                c.getDataNascimento() != null ? c.getDataNascimento().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")) : "",
                c.getCPF(),
                c.getRG(),
                c.getEndereco(),
                String.valueOf(c.getNumeroPredial()),
                c.getBairro(),
                c.getCidade(),
                c.getEstado(),
                c.getCEP(),
                c.getTelefone(),
                c.getDataEntrada() != null ? c.getDataEntrada().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")) : ""
            };
            i++;
        }

        DefaultTableModel model = new DefaultTableModel(dados, colunas);
        tblClientes.setModel(model);
    }
