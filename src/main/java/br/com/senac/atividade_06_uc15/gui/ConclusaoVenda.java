    private void btnAvancarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAvancarActionPerformed
        // TODO add your handling code here:
        String strCPF = txtCPF.getText();
        String strCodigoVendedor = txtCodVendedor.getText();
        String strData = txtData.getText();
        String strValor = txtValor.getText();

        //Teste de validação de entrada de número de CPF: 
        boolean testeCPF = strCPF.matches("[0-9]{3}[.][0-9]{3}[.][0-9]{3}[-][0-9]{2}");

        if (strCodigoVendedor.isEmpty() || strData.isEmpty() || strValor.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos.");
        } else {

            //Teste de validação de entrada de Data:
            String strTesteData = txtData.getText();
            boolean testeData = strTesteData.matches("^(0[1-9]|[12][0-9]|3[01])/(0[1-9]|1[0-2])/(\\d{4})$");

            //Teste de validação de entrada de ID vendedor:
            String strIdVendedor = txtCodVendedor.getText();
            boolean testeIdVendedor = strIdVendedor.matches("\\d+");

            if (testeCPF == true || strCPF.isEmpty() && testeData == true && testeIdVendedor == true) {

                ListaCompras.setCPF(strCPF);
                ListaCompras.setData(strData);
                ListaCompras.setIdVendedor(strCodigoVendedor);

                Pagamento pagamento = new Pagamento();
                pagamento.setVisible(true);
                pagamento.setValorTotal(vlrTotal);
                dispose();

            } else {
                JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos.\n Data: 'dd/mm/aaaa' \n CPF: xxx.xxx.xxx-xx. \n ID Vendedor e CPF válidos.");
            }
        }


    }//GEN-LAST:event_btnAvancarActionPerformed

