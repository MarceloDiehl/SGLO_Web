    private void btnBuscaCPFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaCPFActionPerformed
        // TODO add your handling code here:

        String cpf = txtCPF.getText();
        String testeCPF = "";

        CadastroDAO cadastroClienteDAO = new CadastroDAO();

        int i = 0;
        for (CadastroCliente c : cadastroClienteDAO.listarCliente(cpf)) {

            testeCPF = c.getCPF();

            i++;
        }

        if (testeCPF == "") {
            JOptionPane.showMessageDialog(null, "Cliente não cadastrado.");
        } else {
            JOptionPane.showMessageDialog(null, "Cliente já cadastrado.");
        }


    }//GEN-LAST:event_btnBuscaCPFActionPerformed

