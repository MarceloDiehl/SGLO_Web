    public void atualizarTabela() {
        tblListaCompras.setModel(montarTabela());
    }

    private DefaultTableModel montarTabela() {

        String[] colunas = {"Cód.Produto", "Título", "Valor Unitário", "Quantidade", "Sub-Total"};

        DefaultTableModel tabela = new DefaultTableModel(colunas, 0);

        ArrayList<Compras> lista;
        lista = ListaCompras.listar();

        for (int i = 0; i < lista.size(); i++) {

            Compras c = lista.get(i);
            String[] linha = {
                c.getCodigoProduto(),
                c.getTituloProduto(),
                c.getValorUnitario(),
                c.getQuantidade(),
                c.getValorTotal()};

            tabela.addRow(linha);

        }
        atualizarValorTotal();
        return tabela;
    }


