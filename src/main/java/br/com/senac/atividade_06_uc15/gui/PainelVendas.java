    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        // TODO add your handling code here:

        String strCodigo = txtCodigoProduto.getText();
        String strQuantidade = txtQtdEstoque.getText();
        Integer verificador;
        verificador = ListaCompras.retornarPosicao(strCodigo);

        if (verificador == -1) {
            if (strCodigo.isEmpty()
                    || strQuantidade.isEmpty()) {
                JOptionPane.showMessageDialog(null, "O campo 'código do produto' e o campo 'quantidade' devem ser preenchidos. ");
            } else {
                //Teste de validação de entrada de código do produto:
                String strTesteCodigo = txtCodigoProduto.getText();
                boolean testeCodigo = strTesteCodigo.matches("[0-9]{6}");

                //Teste de validação de entrada de quantidade:
                String strTesteQuantidade = txtQtdEstoque.getText();
                boolean testeQuantidade = strTesteQuantidade.matches("\\d+");

                CadastroProduto p = new CadastroProduto();
                CadastroDAO catalogoProdutoDAO = new CadastroDAO();
                p = catalogoProdutoDAO.buscarPorCodigo(strCodigo);
                int qtd = p.getQuantidade();
                boolean testeEstoque;

                if (qtd < (Integer.parseInt(strQuantidade))) {
                    JOptionPane.showMessageDialog(null, "Não há produtos suficientes no estoque! \n");
                    testeEstoque = false;
                    limparCampos();
                } else {
                    testeEstoque = true;
                }

                if (testeQuantidade == true && testeCodigo == true && testeEstoque == true) {

                    Compras c = new Compras();
                    ListaCompras listaCompras = new ListaCompras();

                    p = catalogoProdutoDAO.buscarPorCodigo(txtCodigoProduto.getText());

                    if (p == null) {
                        JOptionPane.showMessageDialog(null, "Produto não encontrado! \n");
                        limparCampos();

                    } else {

                        String valorTotal;

                        c.setCodigoProduto(p.getCodProduto());
                        c.setTituloProduto(p.getTitulo());
                        c.setValorUnitario(String.valueOf(p.getValor()));
                        c.setQuantidade(txtQtdEstoque.getText());
                        c.setValorTotal(String.valueOf(p.getValor()));

                        listaCompras.adicionar(c);

                        JOptionPane.showMessageDialog(null, "Produto incluido à lista de compras! \n");

                        limparCampos();
                        atualizarTabela();
                        txtCodigoProdutoLista.setText(c.getCodigoProduto());
                        txtDescricao.setText(c.getTituloProduto());
                        txtQtdListaCompras.setText(String.valueOf(tblListaCompras.getRowCount()));
                        atualizarValorTotal();

                    }
                } else {
                    JOptionPane.showMessageDialog(null, "O campo código deverá estar preenchido com 06 dígitos. O campo quantidade deverá ter no mínimo 1 (somente números).");

                }

            }
        } else {
            JOptionPane.showMessageDialog(null, "Produto já se encontra na lista, ajuste a quantidade.");
            txtCodigoProduto.setText("");
            txtQtdEstoque.setText("");

        }

    }//GEN-LAST:event_btnIncluirActionPerformed

