    private Double vlrTotal;

    private void valorTotal(String valor) {
        this.vlrTotal = 0.0;
        if (valor != null && !valor.isEmpty()) {
            this.vlrTotal = Double.parseDouble(valor);
        } else {
            this.vlrTotal = 0.0;
        }
    }

    public void atualizarValorTotal() {
        valorTotal(ListaCompras.getValorTotal());

        NumberFormat formatoMoeda = NumberFormat.getCurrencyInstance(new Locale("pt", "BR"));
        double valor = vlrTotal;
        txtValor.setText(formatoMoeda.format(valor));

    }

