    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblListaCompras.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, selecione uma linha para excluir o produto da lista.");
            return;
        }

        ListaCompras.excluir(linhaSelecionada);
        atualizarValorTotal();

        NumberFormat formatoMoeda = NumberFormat.getCurrencyInstance(new Locale("pt", "BR"));
        double valor = vlrTotal;
        txtValor.setText(formatoMoeda.format(valor));

        atualizarTabela();
        txtQtdListaCompras.setText(String.valueOf(tblListaCompras.getRowCount()));

    }//GEN-LAST:event_btnExcluirActionPerformed

